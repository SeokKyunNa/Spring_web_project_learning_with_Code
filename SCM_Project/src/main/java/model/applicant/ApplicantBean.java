package model.applicant;

public class ApplicantBean {
	private int biz_num;
	private String applicant_id;
	private String apply_date;
	private String applicant_pic;
	private String applicant_age;
	private String applicant_name;
	
	
	public int getBiz_num() {
		return biz_num;
	}
	public void setBiz_num(int biz_num) {
		this.biz_num = biz_num;
	}
	public String getApplicant_id() {
		return applicant_id;
	}
	public void setApplicant_id(String applicant_id) {
		this.applicant_id = applicant_id;
	}
	public String getApply_date() {
		return apply_date;
	}
	public void setApply_date(String apply_date) {
		this.apply_date = apply_date;
	}
	public String getApplicant_pic() {
		return applicant_pic;
	}
	public void setApplicant_pic(String applicant_pic) {
		this.applicant_pic = applicant_pic;
	}
	public String getApplicant_age() {
		return applicant_age;
	}
	public void setApplicant_age(String applicant_age) {
		this.applicant_age = applicant_age;
	}
	public String getApplicant_name() {
		return applicant_name;
	}
	public void setApplicant_name(String applicant_name) {
		this.applicant_name = applicant_name;
	}
	
}